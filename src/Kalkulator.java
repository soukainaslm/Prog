public class Kalkulator {

    public static double addere (double a, double b){
        double sum = a + b;
        return sum;
    }

    public static double substrahere (double c, double d){
        double sum = c - d;
        return sum;

    }

    public static double multiplisere(double e, double f){
        double sum = e * f;
        return sum;
    }
    public static double dividere(double g, double h){
        double sum = g / h;
        return sum;

    }
}
